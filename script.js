// Arrow functions

// Previouls to add we would do the following
function add(a, b) {
    return a + b;
}

// Now with arrow functions (similar to lambda expressions)
const add = (a, b) => a + b;
// "add = (a, b) =>" means the same as "function add(a, b)"

const add = (a, b) => { 
    return a + b;
}

// All the declarations mean the same thing